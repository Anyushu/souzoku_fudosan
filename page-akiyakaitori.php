<dl class="service__wrap__faq__inner">
<dt>売却する場合、どのような費用がかかりますか？</dt>
<dd>売買契約書に貼付する収入印紙代、買い取りの場合の手数料は不要です。仲介の場合は取引成立後仲介手数料（成約価格の３％+６万円）がかかります。また、土地の面積が不明瞭な場合などは測量費用が35万円～50万円程度（土地面積による）や売却物件に抵当権が設定されている場合は抵当権抹消登記にかかる費用が2～3万円程度かかります。</dd>
</dl>
<!-- service__wrap__faq__inner -->
<dl class="service__wrap__faq__inner">
<dt>どのように査定をするのでしょうか？</dt>
<dd>個々の長所、短所、取引相場、路線価、公示価等、減価償却、収益還元法等を考慮して、明確な算出をします。建物がある場合は現地を確認し、独自の買取査定を行います。買取の場合は仲介ではありませんので、不具合の修理や清掃なども不要で、現状有姿のまま買い取りが可能です。</dd>
</dl>
