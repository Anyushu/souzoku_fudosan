<h3 class="ttl__h3 mt-md-5 mt-4">相続財産の内容が不透明</h3>
<p class="small">大きな原因は、相続財産の内容が不透明で正確な情報がないことです。
<br>不透明なために、どれが相続財産になるのかが誰も分からず、相続人同士で他にも遺産がないかと不信感を抱き合うため争いが生じてしまいます。
<br>このトラブルを避ける予防策としては、被相続人が亡くなるまでにきちんと資産を明確にしておく必要があります。
<br>できれば資産目録を作っておき、遺言書も併せて作成して遺産の振り分けを指定してもらうと争わずに済みます。
<br>事前に相続資産についてどうするのかを被相続人と一緒に明確にし、準備しておくのが理想的です。</p>
<p class="small">もし財産目録がない場合は遺産を調査する必要があります。</p>
