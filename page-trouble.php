<h3 class="ttl__h3 mt-md-5 mt-4">遺産の使い道を相続人同士で疑う</h3>
<p class="small">相続人同士が互いに遺産の使い道を疑うことでトラブルが発生することがあります。
<br>例えば、兄弟姉妹のうちの誰かが親と同居している場合、一緒に住んでいる側がお金を使いこんでいたのではないかと疑われることがあります。
<br>そんな事実がなかったとしてももう片方は聞く耳を持たないでしょう。
<br>また、疑いを持たれた側としても面倒を見ていたのはこちらだと言い争いが生じ、余計に揉めてしまう可能性があります。
<br>このトラブルを避ける予防策としては、親と同居する場合に事前に親と相続人の財産をしっかり分けておくことがポイントです。
<br>水道光熱費は誰が払うのかなど、誰がどの支払いをするのか明確にしておくと後でトラブルになっても使い込んだと思われることがありません。
<br>証拠として領収書なども管理しておくのがおすすめです。</p>

<h3 class="ttl__h3 mt-md-5 mt-4">相続人が他の相続人の隠し財産を疑う</h3>
<p class="small">同居している相続人が一部財産をどこかに隠していると疑うケースがあります。
<br>財産の貯金額が少ない場合や口座残高が少ない場合に疑われる可能性が高まります。
<br>このトラブルを避ける予防策としては、先述した通りこの場合も相続内容を明確にしておくことが一番です。
<br>そうすることで隠し財産がないことを証明できるので、トラブルを避けるには遺産目録の作成がカギとなります。</p>

<h3 class="ttl__h3 mt-md-5 mt-4">相続財産の大半が不動産で分けようがない</h3>
<p class="small">預貯金などは相続人の人数によって分割できますが、不動産だと分割することは難しく共有で使用したとしても後に不都合が生じる事があります。
<br>1人が相続したとしても不平等になるのでトラブルが多くなっています。
<br>このトラブルを避ける予防策としては、親の生前に時間を設けきちんと話し合いをし、遺産の分け方を考えておく必要があります。
<br>もし実家を長男に相続させる場合は、兄弟姉妹に他の財産を残すなど、ある程度相続配分が平等になるように工夫が必要です。</p>

<h3 class="ttl__h3 mt-md-5 mt-4">遺産相続トラブルの解決方法</h3>
<p class="small">トラブル発生後、自分たちでどうにか解決しようとしても埒が明かないことが多く、最悪身内の関係が壊れることもあるので、専門家である弁護士に相談するのが一番です。
<br>
<br>弁護士に相談すると以下のようなメリットがあります。
<br>・相続財産、相続人の調査を頼める
<br>・遺産の適切な分け方を教えてもらえる
<br>・相続人同士の交渉を一任できる
<br>・穏便に揉め事を解決できる
<br>・遺留分侵害をされた場合でも遺産を取り戻すことが可能
<br>
<br>相続トラブルが生じたときは専門家である弁護士に相談することで相続争いを収めることができます。
<br>弁護士も様々な分野があるので、相続に特化した専門の弁護士に相談するようにしましょう。</p>
