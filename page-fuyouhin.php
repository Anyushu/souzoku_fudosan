<h3 class="ttl__h3 mb-3 mt-md-5 mt-4">料金形態が明確に提示されているか</h3>
<p class="small">業社を選ぶ際には料金形態がしっかり提示されているかどうかを確認してください。
<br>もし、不用品回収業者が無料で不用品を回収しそのまま不法投棄した場合、回収業者へ適正な対価を払っていないとして利用者の責任と扱われることがあります。
<br>また、様々な理由をつけて費用を請求してくる悪徳業者もおり、トラブルになることがあります。
<br>そのため、万が一無料や安い業者を選ぶ際にはその業者の身元や資格情報をしっかり確認してから依頼するようにしましょう。</p>
<h3 class="ttl__h3 mb-3 mt-md-5 mt-4">書面やデータで無料見積りができるか</h3>
<p class="small">料金が明確で見積りをしっかり作ってくれる業者は信頼性があります。
<br>見積書に費用が掛かる場合は、そのまま依頼へと繋げるための戦略でもあり、後から費用を付け足して請求してくる可能性もあるため注意が必要です。
<br>また、見積もりはできないと拒む企業も怪しいのでそういった企業は候補から外しておくのがおすすめです。</p>
<h3 class="ttl__h3 mb-3 mt-md-5 mt-4">追加でオプションなどの料金がある場合にはしっかり取り決めがあるか</h3>
<p class="small">上記同様、支払いの際にオプション料として別途料金を取られる可能性があります。
<br>そうならないためにも基本料金の取り決め以外に追加料金やオプション料金がないか確認しておきましょう。</p>
